// Math and its Methods


// Basic Arithmetic Operations

console.log(Math.abs(-10));   // 10 (Absolute value)
console.log(Math.sqrt(25));   // 5 (Square root)
console.log(Math.pow(2, 3));  // 8 (2 raised to the power of 3)
console.log(Math.cbrt(8));    // 2 (Cube root of 8)


// Rounding Functions

console.log(Math.round(4.7));   // 5
console.log(Math.floor(4.7));   // 4
console.log(Math.ceil(4.2));    // 5
console.log(Math.trunc(4.7));   // 4 (Removes decimal part)


// Trigonometric Functions

console.log(Math.sin(Math.PI / 2));  // 1 (Sine)
console.log(Math.cos(Math.PI));      // -1 (Cosine)
console.log(Math.tan(Math.PI / 4));  // 1 (Tangent)


// Logarithmic and Exponential Functions

console.log(Math.log(10));     // 2.302... (Natural logarithm)
console.log(Math.log10(100));  // 2 (Base-10 logarithm)
console.log(Math.log2(8));     // 3 (Base-2 logarithm)
console.log(Math.exp(2));      // 7.389... (e raised to the power of 2)


// Min/Max and Random Numbers

console.log(Math.min(2, 8, 1));    // 1
console.log(Math.max(2, 8, 1));    // 8
console.log(Math.random());         // Random number between 0 (inclusive) and 1 (exclusive)
console.log(Math.floor(Math.random() * 10) + 1); // Random integer between 1 and 10


// Hyperbolic Functions

console.log(Math.sinh(0));  // 0
console.log(Math.cosh(0));  // 1
console.log(Math.tanh(0));  // 0


