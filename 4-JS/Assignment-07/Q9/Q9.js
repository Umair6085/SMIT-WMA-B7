let num = prompt('Enter A Week Number :');

if (num == 1) {
    alert("It Is Monday")
    console.log("It Is Monday")
} 
else if(num == 2) {
    alert("It Is Tuesday")
    console.log("It Is Tuesday")
}
 else if(num == 3) {
    alert("It Is Wednesday")
    console.log("It Is Wednesday")
}
 else if(num == 4) {
    alert("It Is Thursday")
    console.log("It Is Thursday")
}
 else if(num == 5) {
    alert("It Is Friday")
    console.log("It Is Friday")
}
 else if(num == 6) {
    alert("It Is Saturday")
    console.log("It Is Saturday")
}
 else if (num == 7) {
    alert("It Is Sunday")
    console.log("It Is Sunday")
}
else {
    alert("Enter Number From 1 - 7")
    console.log("Enter Number From 1 - 7")
}